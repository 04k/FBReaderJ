package org.zlibrary.text.view.impl;

import java.util.ArrayList;

class ZLTextRectangularArea {
	final int XStart;	
	final int XEnd;	
	final int YStart;	
	final int YEnd;	
	
	ZLTextRectangularArea(int xStart, int xEnd, int yStart, int yEnd) {
		XStart = xStart;
		XEnd = xEnd;
		YStart = yStart;
		YEnd = yEnd;
	}
}
